    public int hammingWeight2(int n) {
        int res = 0;
        for (int i = 0; i < 32; i++) {
            if (((n >> i) & 1) == 1) {
                res++;
            }
        }
        return res;
    }

    //åˆ¤æ–­æœ€å1ä½æ˜¯å¦ä¸º1ï¼Œç„¶åå³ç§»1ä½
