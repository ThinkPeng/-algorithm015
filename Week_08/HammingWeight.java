    //æœ€å1ä¸ª1ä¹‹åçš„ä½å˜æˆ1ï¼Œè‡ªå·±è¢«å€Ÿ1å˜æˆ0ï¼Œä¹‹å‰çš„ä¸å˜ï¼Œ& è¿ç®—ä¹‹åï¼Œæœ€åä¸€ä¸ª1ä»¥åŠå…¶åçš„ä½éƒ½ä¸º0ã€‚
    public int hammingWeight(int n) {
        int res = 0;
        while (n != 0) {
            n = n & (n - 1);
            res++;
        }
        return res;
    }

    //for loop : éå† 0 -> 32 ä½
